<script>

import ComFrontnavbar from './FrontPages/ComFrontnavbar';
import ComFrontfooter from './FrontPages/ComFrontfooter';
import $ from 'jquery';

export default {

  components: {
    ComFrontnavbar,
    ComFrontfooter,
  },

  created() {

    //側邊廣告功能

    $(function(){

      $("#asideButton").click(function(event) {
        $("div").toggleClass("asideIn");
      });


      //回頂部標籤功能

      var distBrand = $(".articleLayout").offset().top
      
      $(window).scroll(function(){

        if ( $(this).scrollTop() > distBrand ) {
          $("#backToTopTag").addClass("showToTopTag");
        } else {
          $("#backToTopTag").removeClass("showToTopTag");
        }
    
      })

      $('.backToTop').click(function (e) {
        e.preventDefault();
        $('html,body').animate({
          scrollTop: 0,
        }, 700)
      });
      
      // 有些瀏覽器只支援html，有些只支援body，所以兩個都寫進去


      //新聞展開收合

      $("article").click(function(){
        
        // 取得value屬性值
        var target = $(this).attr("value");

        // 比對p元素
        $(".articleContentLayout p").each(function(){
            // 比對是否符合篩選條件
            if($(this).hasClass(target)){
              // 展開及收合全文
              $(this).toggleClass("ellipsis3")
            }
        })

        $(".articleContentLayout a").each(function(){
            // 比對是否符合篩選條件
            if($(this).hasClass(target)){
              // 展開及收合全文
              $(this).toggleClass("readMoreHidden")
            }
        })

      });

    });

  },

};

</script>






