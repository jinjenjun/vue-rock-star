import ComFrontnavbar from './FrontPages/ComFrontnavbar';
import ComFrontfooter from './FrontPages/ComFrontfooter';
import $ from 'jquery';

export default {

  components: {
    ComFrontnavbar,
    ComFrontfooter,
  },

  created() {

    $(function(){

      //todolist

      //指定dom

        var myName = document.querySelector('.nameText')
        var myMessage = document.querySelector('.messageText')
        var sender = document.querySelector('.sendMessage')
        var showMyList = document.querySelector('.showList')
        var myNameData = JSON.parse(localStorage.getItem('nameData')) || []
        var myMessageData = JSON.parse(localStorage.getItem('messageData')) || []

      //監聽與更新

        sender.addEventListener('click', addData, false)
        showMyList.addEventListener('click', deleteListItem, false)
        updateList(myNameData, myMessageData)
         
      //加入列表、更新網頁和localstorage
      
        function addData(e){

          var nameTxt = myName.value
          var messageTxt = myMessage.value

          var nameAllText = {
            content: nameTxt,
          }

          var messageAllText = {
            content: messageTxt
          }

          myNameData.push(nameAllText)
          myMessageData.push(messageAllText)

          updateList(myNameData, myMessageData)

          localStorage.setItem('nameData', JSON.stringify(myNameData))
          localStorage.setItem('messageData', JSON.stringify(myMessageData))

        }

      //更新網頁內容

      function updateList(items1, items2) {
        var str = ''
        var len = items1.length

        for(var i=0; i<len; i++) {
          str += '<li style="list-style:none" class="pl-0; ml-0">' + (i+6) +'.' + items1[i].content +'<br/><div class="listInnerStyle">' + items2[i].content + '<a data-index=' + i + ' style="position:absolute; right: 22px; cursor: pointer">X</a></div></li>' 
        }

        showMyList.innerHTML = str

      }

      //刪除內容項目

      function deleteListItem(e) {

        if(e.target.nodeName !=='A'){return}
        var index = e.target.dataset.index
        myNameData.splice(index, 1)
        myMessageData.splice(index, 1)

        updateList(myNameData, myMessageData)

        localStorage.setItem('nameData', JSON.stringify(myNameData))
        localStorage.setItem('messageData', JSON.stringify(myMessageData))

      }

      //防止todolist空白

      var inputName=document.getElementById('input_name')       
      inputName.onblur=function(){
        if(inputName.value.trim()==null || inputName.value.trim()==""){
          console.log('請填入暱稱')
          document.getElementById('sendcontent').disabled = true
          return false 
        }else{
          document.getElementById('sendcontent').disabled = false
        }
      };

      var inputMessage=document.getElementById('input_message')
      inputMessage.onblur=function(){
        if(inputMessage.value.trim()==null || inputMessage.value.trim()==""){
          console.log('請填入留言內容')
          document.getElementById('sendcontent').disabled = true
          return false       
        }else{
          document.getElementById('sendcontent').disabled = false
        }
      };

      //冰山移動特效

      $(window).mousemove(function(event){

        var pagex=event.pageX;
        var pagey=event.pageY;
        
        var x=pagex-$("#aboutwubai").offset().left;
        var y=pagey-$("#aboutwubai").offset().top;
                  
        console.log(x+","+y)
                  
        $(".jumbotronMou001").css("transform","translateX("+(pagex/-50)+"px)")
        $(".jumbotronMou002").css("transform","translateX("+(pagex/-50)+"px)") 
        $(".jumbotronMou003").css("transform","translateX("+(pagex/50)+"px)")
        $(".jumbotronMou004").css("transform","translateX("+(pagex/50)+"px)")

        $(".GroupMou001").css("transform","translateX("+(pagex/-50)+"px)")
        $(".GroupMou002").css("transform","translateX("+(pagex/-50)+"px)") 
        $(".GroupMou003").css("transform","translateX("+(pagex/50)+"px)")
        $(".GroupMou004").css("transform","translateX("+(pagex/50)+"px)")

        $(".GroupMou005").css("transform","translateX("+(pagex/-50)+"px)")
        $(".GroupMou006").css("transform","translateX("+(pagex/-50)+"px)")
        $(".GroupMou007").css("transform","translateX("+(pagex/50)+"px)")

      });

      //幽浮浮標特效

      var mouseImg = document.querySelector('.mouseImg');
      function getPosition(e) {                 

        mouseImg.style.left =e.clientX+'px';
        mouseImg.style.top =e.clientY+'px';
        
      }

      var el = document.body;      
      el.addEventListener('mousemove', getPosition, false); 

      $("#messageZone").hover(
        function(){
          $(".mouseImg").css("z-index","-1")
        },function(){
          $(".mouseImg").css("z-index","3")
        }
      )

      $("#backToTopTag").hover(
        function(){
          $(".mouseImg").css("z-index","-1")
        },function(){
          $(".mouseImg").css("z-index","3")
        }
      )

      //回頂部標籤功能

      var distBrand = $(".GroupAllOutside").offset().top
      
      $(window).scroll(function(){

        if ( $(this).scrollTop() > distBrand ) {
          $("#backToTopTag").addClass("showToTopTag");
        } else {
          $("#backToTopTag").removeClass("showToTopTag");
        }
    
      })

      $('.backToTop').click(function (e) {
        e.preventDefault();
        $('html,body').animate({
          scrollTop: 0,
        }, 700)
      });
      
      // 有些瀏覽器只支援html，有些只支援body，所以兩個都寫進去

      
      // 團員資料點擊特效
      $(".GroupMemberOutsideTag").click(
        function(e){
          e.preventDefault();
          $(this).find(".GroupMemberContent").toggleClass("GroupMemberContentShow");
          $(this).siblings().find(".GroupMemberContent").removeClass("GroupMemberContentShow");
        },
      );
      

    });

  },
  

};

